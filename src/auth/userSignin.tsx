import {API} from '../Api'

const signin = (user: any) => {
    return fetch(`${API}/signIn`, {
      method: "POST",
      headers: {
        Accept: "application/json",
        "Content-Type": "application/json"
      },
      body: JSON.stringify(user)
    })
      .then(response => {
        return response.json();
      })
      .catch(err => console.log(err));
  };

export default signin;